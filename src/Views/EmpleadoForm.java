    private void tablaAcessoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaAcessoMouseClicked
        int fila = tablaAcesso.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar una fila");
        } else {
            int nss = Integer.parseInt(tablaAcesso.getValueAt(fila, 1).toString());
            String nom = tablaAcesso.getValueAt(fila, 2).toString();
            String ape = tablaAcesso.getValueAt(fila, 3).toString();
            String rol = tablaAcesso.getValueAt(fila, 4).toString();
            String email = tablaAcesso.getValueAt(fila, 5).toString();
            long telefono = Long.parseLong(tablaAcesso.getValueAt(fila, 6).toString());

            txtNSS.setText(String.valueOf(nss));
            txtNombre.setText(String.valueOf(nom));
            txtApellidos.setText(ape);
            txtRol.setText(rol);
            txtEmail.setText(email);
            txtTelefono.setText(telefono+"");
        }
    }//GEN-LAST:event_tablaAcessoMouseClicked

    void limpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }

    void actualizar() {
        int fila = tablaAcesso.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un registro");
        } else {
            int nss = Integer.parseInt(txtNSS.getText());
            String nom = txtNombre.getText();
            String ape = txtApellidos.getText();
            String rol = txtRol.getText();
            String email = txtEmail.getText();
            String pass = txtContrasenia.getText();
            long telefono = Long.parseLong(txtTelefono.getText());
            Object[] ob = new Object[7];
            if (nom != null && ape != null && rol != null && email != null && pass != null) {
                ob[0] = nss;
                ob[1] = nom;
                ob[2] = ape;
                ob[3] = rol;
                ob[4] = email;
                ob[5] = pass;
                ob[6] = telefono;

                if (peDAO.actualizar(ob) > 0) {
                    JOptionPane.showMessageDialog(null, "Datos Actualizados");
                    nuevo();
                }else{
                    JOptionPane.showMessageDialog(null, "Hubo un error");
                }

            }
        }
    }

