    void agregar() {

        Producto p = new Producto();
        int cat = Integer.parseInt(txtCategoria.getText());
        ProductoDAO pDAO = new ProductoDAO();
        String nom = txtProducto.getText();
        String desc = txtDescripcion.getText();
        String marca = txtMarca.getText();
        int stock = Integer.parseInt(txtStock.getValue().toString());
        String img = txtImg.getText();

        Object[] ob = new Object[7];

        if (nom != null && desc != null && marca != null && stock != 0 && img != null && stock > 0) {
            double precio = Double.parseDouble(txtPrecio.getText());
            ob[0] = cat;
            ob[1] = nom;
            ob[2] = desc;
            ob[3] = marca;
            ob[4] = stock;
            ob[5] = precio;
            ob[6] = img;

            pDAO.add(ob);
            nuevo();
        } else {
            JOptionPane.showMessageDialog(this, "Es necesario rellenar todos los campos");
        }

    }

    void limpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }

    void actualizar() {
        int fila = tablaProd.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un registro");
        } else {
            int id_producto = Integer.parseInt(txtCodigo.getText());
            int id_categoria = Integer.parseInt(txtCategoria.getText());
            String nom = txtProducto.getText();
            String desc = txtDescripcion.getText();
            String marca = txtMarca.getText();
            int stock = Integer.parseInt(txtStock.getValue().toString());
            String img = txtImg.getText();
            Object[] ob = new Object[8];
            if (nom != null && desc != null && marca != null && stock != 0 && img != null) {
                double precio = Double.parseDouble(txtPrecio.getText());
                ob[0] = id_producto;
                ob[1] = id_categoria;
                ob[2] = nom;
                ob[3] = desc;
                ob[4] = marca;
                ob[5] = stock;
                ob[6] = precio;
                ob[7] = img;

                pDAO.actualizar(ob);
                JOptionPane.showMessageDialog(null, "Datos Actualizados");
                nuevo();
            };
        }
    }

