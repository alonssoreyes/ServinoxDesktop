    void listar() {
        List<Empleado> lista = peDAO.getEmpleados();
        modelo = (DefaultTableModel) tablaAcesso.getModel();
        Object[] ob = new Object[8];

        for (int i = 0; i < lista.size(); i++) {
            ob[0] = lista.get(i).getId();
            ob[1] = lista.get(i).getNss();
            ob[2] = lista.get(i).getNombre();
            ob[3] = lista.get(i).getApellidos();
            ob[4] = lista.get(i).getPuesto();
            ob[5] = lista.get(i).getEmail();
            ob[6] = lista.get(i).getTelefono();
            ob[7] = lista.get(i).getEntrada();

            modelo.addRow(ob);
        }
        tablaAcesso.setModel(modelo);

    }

